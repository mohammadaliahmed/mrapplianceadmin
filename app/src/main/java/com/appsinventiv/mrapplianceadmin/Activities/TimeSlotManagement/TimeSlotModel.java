package com.appsinventiv.mrapplianceadmin.Activities.TimeSlotManagement;

public class TimeSlotModel {
    String id,serviceName,year,month,date,time;

    public TimeSlotModel(String id, String serviceName, String year, String month, String date, String time) {
        this.id = id;
        this.serviceName = serviceName;
        this.year = year;
        this.month = month;
        this.date = date;
        this.time = time;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getServiceName() {
        return serviceName;
    }

    public void setServiceName(String serviceName) {
        this.serviceName = serviceName;
    }

    public String getYear() {
        return year;
    }

    public void setYear(String year) {
        this.year = year;
    }

    public String getMonth() {
        return month;
    }

    public void setMonth(String month) {
        this.month = month;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }
}
